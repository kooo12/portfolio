class PersonalInfo {
  final String name;
  final String title;
  final String bio;
  final String email;
  final String phone;
  final String location;
  final String imageUrl;
  final String resumeUrl;
  final Map<String, String> socialLinks;

  PersonalInfo({
    required this.name,
    required this.title,
    required this.bio,
    required this.email,
    required this.phone,
    required this.location,
    required this.imageUrl,
    required this.resumeUrl,
    required this.socialLinks,
  });
}
